public class first {
  public static void main(String[] args) {
    String x1 = "100";
    int val1 = Integer.parseInt(x1);
    System.out.println(val1);

    String x2 = "9.9";
    float val2 = Float.parseFloat(x2);
    System.out.println(val2);

    String x3 = "99.99";
    double val3 = Double.parseDouble(x3);
    System.out.println(val3);

    String x4 = "false";
    boolean val4 = Boolean.parseBoolean(x4);
    System.out.println(val4);
  }
}
